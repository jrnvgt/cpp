void write(int number) {
    int digits[10] = {};
    int length = 0;

    while (number) {
        digits[length] = number % 10;
        number /= 10;
        length++;
    }

    for (int i = 0; i < 5; i++) {
        for (int j = length - 1; j >= 0; j--) {
            std::cout << bigDigits[digits[j]][i] << " ";
        }

        std::cout << std::endl;
    }
}
